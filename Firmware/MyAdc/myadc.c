//-----------------------------------------------------------------------------
// Task Dispatcher hooks
//   The following hooks are called by the task dispatcher.
//-----------------------------------------------------------------------------

void GpifInit( void );

void TD_Init(void)             // Called once at startup
{
   CPUCS = ((CPUCS & ~bmCLKSPD) | bmCLKSPD1) ;	// 48 MHz CPU clock

   REVCTL = 0x03; // REVCTL.0 and REVCTL.1 set to 1
   SYNCDELAY;

   EP2CFG = 0xE8; // IN, Bulk, 1024, Quad

   GpifInit();

   // start GPIF FIFO Read
   FIFORESET = 0x80; // activate NAK-ALL to avoid race conditions
   SYNCDELAY;

   EP2FIFOCFG = 0x00; //switching to manual mode
   SYNCDELAY;
   FIFORESET = 0x02; // Reset FIFO 2
   SYNCDELAY;
   EP2FIFOCFG = 0x08; //switching to auto mode
   SYNCDELAY;

   FIFORESET = 0x00; //Release NAKALL
   SYNCDELAY;

   EP2GPIFFLGSEL = 2; // GPIF FIFOFlag is full
   SYNCDELAY;

	GPIFTCB0 = 1;
	SYNCDELAY;
   // trigger FIFO read transactions, using SFR
   GPIFTRIG = bmBIT2 | 0; // R/W=1, EP[1:0]=FIFO_EpNum
}

